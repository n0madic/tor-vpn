/// Platform-specific config directory for the UI application.
fn config_dir() -> PathBuf {
    #[cfg(target_os = "macos")]
    {
        let home = std::env::var("HOME").unwrap_or_else(|_| "/tmp".to_string());
        PathBuf::from(home).join("Library/Application Support/com.tor-vpn.ui")
    }
    #[cfg(target_os = "linux")]
    {
        let config = std::env::var("XDG_CONFIG_HOME").unwrap_or_else(|_| {
            let home = std::env::var("HOME").unwrap_or_else(|_| "/tmp".to_string());
            format!("{home}/.config")
        });
        PathBuf::from(config).join("tor-vpn-ui")
    }
    #[cfg(target_os = "windows")]
    {
        let appdata = std::env::var("APPDATA").unwrap_or_else(|_| "C:\\".to_string());
        PathBuf::from(appdata).join("tor-vpn-ui")
    }
}

fn config_file_path() -> PathBuf {
    config_dir().join("config.json")
}

fn find_daemon_path() -> String {
    // 1. Check for bundled sidecar (next to the UI executable)
    if let Some(path) = find_sidecar_path() {
        return path;
    }
    // 2. Fallback: search PATH
    which::which("tor-vpn")
        .map(|p| p.to_string_lossy().into_owned())
        .unwrap_or_else(|_| "tor-vpn".to_string())
}

/// Check for a bundled sidecar binary next to the UI executable.
///
/// Tauri places `externalBin` binaries in the same directory as the main
/// executable on all platforms (macOS: `App.app/Contents/MacOS/`, Linux:
/// `/usr/bin/`, Windows: install dir).
fn find_sidecar_path() -> Option<String> {
    let exe = std::env::current_exe().ok()?;
    let dir = exe.parent()?;
    let name = if cfg!(windows) {
        "tor-vpn.exe"
    } else {
        "tor-vpn"
    };
    let sidecar = dir.join(name);
    if sidecar.is_file() {
        Some(sidecar.to_string_lossy().into_owned())
    } else {
        None
    }
}

/// Resolve a setting from daemon.conf, falling back to a default value.
///
/// Reads and parses `daemon.conf` once, then extracts the desired field
/// via the `extractor` closure. Returns the default if the file doesn't
/// exist, can't be parsed, or the field is `None`.
pub fn resolve_daemon_setting<T>(
    extractor: impl FnOnce(&config::ConfigFile) -> Option<T>,
    default: impl FnOnce() -> T,
) -> T {
    let conf = daemon_config_path();
    if conf.exists() {
        if let Ok(parsed) = config::parse_config_file(&conf) {
            if let Some(value) = extractor(&parsed) {
                return value;
            }
        }
    }
    default()
}

/// Load VPN state from the daemon's state file.
///
/// Checks daemon.conf for a custom `StateFile` directive, falling back
/// to the platform default.
pub fn load_vpn_state() -> Option<VpnState> {
    let path = resolve_daemon_setting(|c| c.state_file.clone(), default_state_file);
    let content = std::fs::read_to_string(path).ok()?;
    serde_json::from_str(&content).ok()
}

// ---------------------------------------------------------------------------
// Tauri commands
// ---------------------------------------------------------------------------

/// Load full config: `daemon_path` from `config.json`, daemon fields from `daemon.conf`.
#[tauri::command]
pub fn get_config() -> Result<UiConfig, String> {
    let settings = load_ui_settings();
    let daemon = load_daemon_settings();
    Ok(UiConfig {
        daemon_path: settings.daemon_path,
        minimize_to_tray: settings.minimize_to_tray,
        disconnect_on_quit: settings.disconnect_on_quit,
        daemon,
    })
}

/// Save config: UI fields → `config.json`, daemon fields → `daemon.conf`.
#[tauri::command]
pub fn save_config(config: UiConfig) -> Result<(), String> {
    save_ui_settings(&UiSettings {
        daemon_path: config.daemon_path,
        minimize_to_tray: config.minimize_to_tray,
        disconnect_on_quit: config.disconnect_on_quit,
    })?;
    save_daemon_conf(&config.daemon)
}

/// Read saved daemon_path from config.json (same path the button uses).
pub fn saved_daemon_path() -> String {
    load_ui_settings().daemon_path
}

/// Read minimize_to_tray setting (for window close handler).
pub fn should_minimize_to_tray() -> bool {
    load_ui_settings().minimize_to_tray
}

/// Read disconnect_on_quit setting (for app exit handler).
pub fn should_disconnect_on_quit() -> bool {
    load_ui_settings().disconnect_on_quit
}

#[tauri::command]
pub fn get_daemon_path() -> String {
    find_daemon_path()
}

