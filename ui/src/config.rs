use std::path::PathBuf;

use serde::{Deserialize, Serialize};

pub use state::VpnState;
pub use state::{default_state_file, is_tor_vpn_process};

/// Status reported to the frontend via events and commands.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "status")]
pub enum VpnStatus {
    #[serde(rename = "disconnected")]
    Disconnected,
    #[serde(rename = "connected")]
    Connected {
        state: VpnState,
        uptime_secs: u64,
        tx_rate: f64,
        rx_rate: f64,
    },
    #[serde(rename = "dirty")]
    Dirty { state: VpnState },
}

/// Full configuration exposed to the frontend via Tauri IPC.
///
/// Assembled from two sources on load:
/// - `daemon_path` from `config.json` (UI-only setting)
/// - All daemon fields from `daemon.conf` via [`config::Config`]
///
/// `#[serde(flatten)]` keeps the JSON shape flat — frontend sees one object.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct UiConfig {
    pub daemon_path: String,
    #[serde(default)]
    pub minimize_to_tray: bool,
    #[serde(default = "default_true")]
    pub disconnect_on_quit: bool,
    #[serde(flatten)]
    pub daemon: config::Config,
}

fn default_true() -> bool {
    true
}

impl Default for UiConfig {
    fn default() -> Self {
        Self {
            daemon_path: find_daemon_path(),
            minimize_to_tray: false,
            disconnect_on_quit: true,
            daemon: config::Config::default(),
        }
    }
}

// ---------------------------------------------------------------------------
// UI-only settings (config.json)
// ---------------------------------------------------------------------------

/// Settings persisted to `config.json` — UI-only, not passed to the daemon.
#[derive(Debug, Clone, Serialize, Deserialize)]
struct UiSettings {
    daemon_path: String,
    /// Hide window to tray instead of minimizing to taskbar (default: false).
    #[serde(default)]
    minimize_to_tray: bool,
    /// Disconnect VPN daemon when the UI app quits (default: true).
    #[serde(default = "default_true")]
    disconnect_on_quit: bool,
}

impl Default for UiSettings {
    fn default() -> Self {
        Self {
            daemon_path: find_daemon_path(),
            minimize_to_tray: false,
            disconnect_on_quit: true,
        }
    }
}

fn load_ui_settings() -> UiSettings {
    let path = config_file_path();
    if path.exists() {
        if let Ok(content) = std::fs::read_to_string(&path) {
            if let Ok(settings) = serde_json::from_str(&content) {
                return settings;
            }
        }
    }
    UiSettings::default()
}

fn save_ui_settings(settings: &UiSettings) -> Result<(), String> {
    let dir = config_dir();
    std::fs::create_dir_all(&dir).map_err(|e| format!("Failed to create config dir: {e}"))?;
    let json = serde_json::to_string_pretty(settings)
        .map_err(|e| format!("Failed to serialize settings: {e}"))?;
    std::fs::write(config_file_path(), json).map_err(|e| format!("Failed to write settings: {e}"))
}

// ---------------------------------------------------------------------------
// Daemon config (daemon.conf) — torrc-style, single source of truth
// ---------------------------------------------------------------------------

/// Path for the daemon config file generated by the UI.
pub fn daemon_config_path() -> PathBuf {
    config_dir().join("daemon.conf")
}

/// Log file path for the daemon (used in daemon.conf and by log reader).
///
/// Uses hardcoded `/tmp/` on macOS (not `std::env::temp_dir()` which is per-user —
/// the daemon runs as root but the UI reads the log as a regular user).
pub fn daemon_log_path() -> PathBuf {
    #[cfg(target_os = "macos")]
    {
        return PathBuf::from("/tmp/tor-vpn-daemon.log");
    }
    #[allow(unreachable_code)]
    std::env::temp_dir().join("tor-vpn-daemon.log")
}

/// Load daemon settings from `daemon.conf`, returning defaults if not present.
fn load_daemon_settings() -> config::Config {
    let path = daemon_config_path();
    if !path.exists() {
        return config::Config::default();
    }
    match config::parse_config_file(&path) {
        Ok(parsed) => config::Config::from(parsed),
        Err(e) => {
            eprintln!("[tor-vpn-ui] Warning: failed to parse daemon.conf: {e}");
            config::Config::default()
        }
    }
}

/// Save daemon settings to `daemon.conf`.
fn save_daemon_conf(settings: &config::Config) -> Result<(), String> {
    let path = daemon_config_path();
    let content = settings.to_config_content(&daemon_log_path());

    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent).map_err(|e| format!("Failed to create config dir: {e}"))?;
    }

    std::fs::write(&path, &content).map_err(|e| format!("Failed to write daemon config: {e}"))
}

