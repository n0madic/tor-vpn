#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_default_config() {
        let config = UiConfig::default();
        assert_eq!(config.daemon.tun_name, "torvpn0");
        assert_eq!(
            config.daemon.tun_address,
            "10.200.0.1".parse::<std::net::Ipv4Addr>().unwrap()
        );
        assert_eq!(config.daemon.tun_netmask, 24);
        assert_eq!(config.daemon.tun_mtu, 1500);
        assert!(matches!(
            config.daemon.isolation,
            config::IsolationPolicy::PerDestination
        ));
        assert_eq!(config.daemon.max_connections, 256);
        assert_eq!(config.daemon.state_write_interval, 60);
        assert!(!config.daemon.override_dns);
    }

    #[test]
    fn test_vpn_state_deserialization() {
        let json = r#"{"pid":1234,"tun_name":"utun7","original_gateway":"192.168.1.1","original_interface":"en0","guard_ips":["1.2.3.4"],"bypass_cidrs":[],"tx_bytes":1024,"rx_bytes":2048}"#;
        let state: VpnState = serde_json::from_str(json).unwrap();
        assert_eq!(state.pid, 1234);
        assert_eq!(state.tx_bytes, 1024);
        assert_eq!(state.rx_bytes, 2048);
    }

    #[test]
    fn test_vpn_status_serialization() {
        let status = VpnStatus::Disconnected;
        let json = serde_json::to_string(&status).unwrap();
        assert!(json.contains("\"status\":\"disconnected\""));

        let status = VpnStatus::Dirty {
            state: VpnState::default(),
        };
        let json = serde_json::to_string(&status).unwrap();
        assert!(json.contains("\"status\":\"dirty\""));
    }

    #[test]
    fn test_ui_settings_serialization() {
        let settings = UiSettings {
            daemon_path: "/usr/local/bin/tor-vpn".to_string(),
            minimize_to_tray: false,
            disconnect_on_quit: true,
        };
        let json = serde_json::to_string(&settings).unwrap();
        assert!(json.contains("daemon_path"));
        assert!(json.contains("disconnect_on_quit"));
        assert!(!json.contains("tun_name"));
    }

    /// Verify serde(flatten) produces the flat JSON shape the frontend expects.
    #[test]
    fn test_ui_config_flat_json() {
        let config = UiConfig::default();
        let json = serde_json::to_string(&config).unwrap();
        // All fields at top level, no nested "daemon" key
        assert!(json.contains("\"daemon_path\""));
        assert!(json.contains("\"tun_name\""));
        assert!(json.contains("\"isolation\""));
        assert!(json.contains("\"socket_path\""));
        assert!(!json.contains("\"daemon\""));
    }

    /// Verify flat JSON from frontend deserializes correctly.
    #[test]
    fn test_ui_config_flat_json_roundtrip() {
        let json = r#"{
            "daemon_path": "/usr/bin/tor-vpn",
            "tun_name": "mytun",
            "tun_address": "10.100.0.1",
            "tun_netmask": 16,
            "tun_mtu": 1400,
            "isolation": "session",
            "cache_dir": "/tmp/cache",
            "bypass_cidrs": ["10.0.0.0/8"],
            "max_connections": 512,
            "max_dns_queries": 128,
            "dns_cache_ttl": 300,
            "override_dns": true,
            "exit_country": "US",
            "bridges": ["obfs4 1.2.3.4:443 AAAA"],
            "pt_paths": ["obfs4=/usr/bin/obfs4proxy"],
            "socket_path": "/tmp/custom.sock",
            "state_write_interval": 5
        }"#;
        let config: UiConfig = serde_json::from_str(json).unwrap();
        assert_eq!(config.daemon_path, "/usr/bin/tor-vpn");
        assert_eq!(config.daemon.tun_name, "mytun");
        assert_eq!(config.daemon.isolation, config::IsolationPolicy::Session);
        assert_eq!(config.daemon.bridges.len(), 1);
        assert_eq!(
            config.daemon.socket_path,
            std::path::PathBuf::from("/tmp/custom.sock")
        );
    }
}
